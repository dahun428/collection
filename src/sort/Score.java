package sort;

public class Score {

	private int no;
	private String name;
	private int total;
	
	public Score(int no, String name, int total) {
		super();
		this.no = no;
		this.name = name;
		this.total = total;
	}
	public Score() {
	}
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
	
	
	
}
